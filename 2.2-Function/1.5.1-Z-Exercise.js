function reverse() {

    var num = '123';

    var str = "";

    for (var i = num.length - 1; i >= 0; i--) {

        str += transfer(num[i]);
    }
}

function transfer(target) {

    switch (target) {

        case '1':
            return '一';
        case '2':
            return '二';
        case '3:':
            return '三';
    }
}


