// 斐波那契数列


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - //


function fb(n) {

    if (n == 1 || n == 2) {

        return 1;
    }

    return fb(n - 1) + fb(n - 2);
}


