//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - //


function factorial(n) {


    // var num = 1;
    //
    // for (var i = 1; i <= n; i++) {
    //
    //     num +=  1;
    // }


    if (n == 1 || n == 0) {

        return 1;
    }

    return n * factorial(n - 1);

}

