// 求数组中的最大值


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


function maxOfArray(arr) {

    if (arr.length === 0) {

        return false;
    }

    let max = arr[0];

    for (var i = 0; i < arr.length; i++) {

        if (arr[i] > max) {

            max = arr[i];
        }
    }

    return max;
}


