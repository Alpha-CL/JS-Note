/**
 *
 * 浅拷贝
 *
 */

//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


var obj1 = {

    age: 10,
    sex: 'male',
    car: ['red', 'blue', 'green', 'yellow']
};

var obj2 = {};

/**
 *
 * 浅拷贝: 把一个对象的属性复制到另一个对象中
 *
 */

function extend(a, b) {

    for (var key in a) {

        b[key] = a[key];
    }
}

extend(obj1, obj2);

console.dir(obj2);          //开始时是空数组
console.dir(obj1);          //有属性


